package akosha.demo.model;



public class AppAuthentication {

	private String appid;

	private String app_secretid;
	private String app_permissions;
	public String getAppid() {
		return appid;
	}
	public void setAppid(String appid) {
		this.appid = appid;
	}
	public String getApp_secretid() {
		return app_secretid;
	}
	public void setApp_secretid(String app_secretid) {
		this.app_secretid = app_secretid;
	}
	public String getApp_permissions() {
		return app_permissions;
	}
	public void setApp_permissions(String app_permissions) {
		this.app_permissions = app_permissions;
	}
	
	

}
